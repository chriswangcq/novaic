    """Parse stored content: JSON {"text","attachments"} or plain string -> dict."""
    if content is None:
        return {"text": "", "attachments": []}
    if isinstance(content, dict):
        return content
    s = str(content).strip()
    if not s:
        return {"text": "", "attachments": []}
