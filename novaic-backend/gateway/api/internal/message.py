    msg = message_repo.add_message(
        agent_id=agent_id,
        type="SUBAGENT_COMPLETED",
        content=content,
        metadata={
            "subagent_id": subagent_id,
            "parent_subagent_id": parent_subagent_id,
            "result": result,
        },
        status="sending",  # Watchdog will process this
    )
    
